# Declaração das variáveis
a = 1
b = 3

# Operação com maior ( > )
print('a é maior que b?\n' + str( a > b ) )

# Operação com menor ( < )
print('\na é maior que b?\n' + str( a < b ) )

# Operação com igual ( == )
print('\na é maior que b?\n' + str( a == b ) )

# Operação com diferente ( != )
print('\na é maior que b?\n' + str( a != b ) )

# Operação com maior ou igual ( >= )
print('\na é maior que b?\n' + str( a >= b ) )

# Operação com menor ou igual ( <= )
print('\na é maior que b?\n' + str( a <= b ) )