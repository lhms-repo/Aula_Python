# String é um array unidimensional de caracteres

# Tipos de declarações de strings (são iguais):
string1 = "string"
string_2 = 'string'
print('{string1} é igual a {string_2}? {string1 == string_2}')
print(f'{string1} é igual a {string_2}? {string1 == string_2}')

# Embora sejam muito úteis, consomem mais memória que os
#   demais tipos de variáveis