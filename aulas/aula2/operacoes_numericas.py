# Soma de números em Python
resultado = 1 + 2
print('Resultado da soma: '+ str(resultado) )

# Subtração de números em Python
resultado = 10 - 20
print('Resultado da substração: '+ str(resultado) )

# Multiplicação de números em Python
resultado = 10 * 30
print('Resultado da multiplicação: '+ str(resultado) )

# Divisão de números em Python
resultado = 1 / 2
print('Resultado da divisão: '+ str(resultado) )

# Potenciação de números em Python
resultado = 2 ** 3 # Que é igual a 2 * 2 * 2
print('Resultado da potenciação: '+ str(resultado) )